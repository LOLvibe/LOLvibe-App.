#import "CustomFonts.h"

@implementation CustomFonts

+ (UIFont *)OpenSans_Bold_Size:(CGFloat)fSize{
    return [UIFont fontWithName:@"OpenSans-Bold" size:fSize];
}

+ (UIFont *)OpenSans_Light_Size:(CGFloat)fSize{
    return [UIFont fontWithName:@"OpenSans-Light" size:fSize];
}

+ (UIFont *)OpenSans_Regular_Size:(CGFloat)fSize{
    return [UIFont fontWithName:@"OpenSans-Regular" size:fSize];
}

+ (UIFont *)OpenSans_SemiBold_Size:(CGFloat)fSize{
    return [UIFont fontWithName:@"OpenSans-Semibold" size:fSize];
}

@end
